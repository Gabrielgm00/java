    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

  if (txtTitulo.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "o campo Titulo esta vazio");
            txtTitulo.requestFocus();
            return;
        } else if (txtEscritor.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "o campo Escritor esta vazio");
            txtEscritor.requestFocus();
            return;
        } else if (txtEditora.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "o campo Editora esta vazio");
            txtEditora.requestFocus();
            return;
        } else if (txtGenero.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "o campo Genero esta vazio");
            txtGenero.requestFocus();
            return;
        }else if (txtAno.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "o campo Ano esta vazio");
            txtAno.requestFocus();
            return;
        }else if (txtQuantidade.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "o campo Quantidade esta vazio");
            txtQuantidade.requestFocus();
            return;
        }



        try {
            Connection con;
            PreparedStatement ps;
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/cadastrobanco", "root", "123gabriel123");

            ps = con.prepareStatement("INSERT INTO livros VALUES(?, ?, ?,?,?,?)");
            ps.setString(1, txtTitulo.getText());
            ps.setString(2, txtEscritor.getText());
            ps.setString(3, txtEditora.getText());
             ps.setString(4, txtGenero.getText());
            ps.setInt(5, Integer.parseInt(txtAno.getText()));
            ps.setInt(6, Integer.parseInt(txtQuantidade.getText()));
           
            ps.executeUpdate();

            JOptionPane.showMessageDialog(null, "dados salvos com sucesso");
            txtTitulo.setText("");
            txtEscritor.setText("");
            txtEditora.setText("");
            txtAno.setText("");
            txtQuantidade.setText("");
            txtGenero.setText("");
            txtTitulo.requestFocus();

        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Erro " + ex.getMessage() + "\nEntre em contato com o administrador e informe");
        } catch (SQLException ex) {
            if (ex.getErrorCode() == 1062) {
                JOptionPane.showMessageDialog(null, "O Nome que você usou ja esta cadastrador tente outro");
                txtTitulo.requestFocus();
            } else {
                JOptionPane.showMessageDialog(null, "error numero" + ex.getErrorCode() + "\nEntre em contato com o administrador e informe o número do erro");
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

